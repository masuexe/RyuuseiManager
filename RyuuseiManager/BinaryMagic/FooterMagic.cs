namespace RyuuseiManager.BinaryMagic
{
    public class FooterMagic
    {
        // Footer Magic
        // Next byte after the common footer header should be this:
        // 0x12: SF1
        // 0x14: SF2 N/S
        // 0x17: SF3
        // 0x18: SF2 Z
        // Importing save data from opposite version with exclusive giga card is considered illegal.
        public static readonly byte[] Eof = new byte[] { 0x5A, 0x5A, 0x94, 0x27, 0x07, 0x00, 0x00, 0x00, 0x04, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00 };

        public static readonly byte[] SwitchSaveFooterMagic = new byte[] {
            0x47, 0x03, 0x90, 0x79, 0x02, 0x00, 0x00, 0x00, 0xB6, 0x95, 0x5D, 0x91, 0x55, 0x64, 0xFB, 0x02, 
            0x02, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x73, 0xCD, 0x3C, 0x77,
            0x09, 0x00, 0x00, 0x00, 0x08, 0x00, 0x00, 0x00};

        public class SF1
        {
            public static readonly byte[] MessageFooterMagic = new byte[] { 0x6D, 0x42, 0x15, 0xF6, 0x0F, 0x00, 0x00, 0x00 };
            public static readonly byte[] SecretFooterMagic = new byte[] { 0x24, 0xD6, 0xA5, 0x60, 0x09, 0x00, 0x00, 0x00 };
            public static readonly byte[] SwitchFooterMagic = new byte[] { 0xEA, 0x27, 0x25, 0x72, 0xE6, 0x8C, 0xDE, 0x08 };
        }

        public class SF2
        {
            public static readonly byte[] MessageFooterMagic = new byte[] { 0x6D, 0x42, 0x15, 0xF6, 0x0F, 0x00, 0x00, 0x00 };
            public static readonly byte[] SecretFooterMagic = new byte[] { 0x68, 0xE7, 0x41, 0xFC, 0x11, 0x00, 0x00, 0x00 };
            public static readonly byte[] SwitchFooterMagic = new byte[] { 0xD7, 0x1B, 0x4D, 0xA2, 0x7B, 0x9C, 0xDE, 0x08 };
        }

        public class SF3
        {
            public static readonly byte[] MessageFooterMagic = new byte[] { 0x68, 0xE7, 0x41, 0xFC, 0x11, 0x00, 0x00, 0x00 };
            public static readonly byte[] TeamNameFooterMagic = new byte[] { 0x3A, 0xC5, 0xA4, 0x62, 0x0F, 0x00, 0x00, 0x00 };
            public static readonly byte[] SwitchFooterMagic = new byte[] { 0x4D, 0x97, 0x83, 0xD7, 0x4A, 0xA7, 0xDE, 0x08 };
        }
    }
